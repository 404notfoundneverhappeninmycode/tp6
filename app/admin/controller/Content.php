namespace app\admin\controller;

use app\common\model\Cate as M;

use think\facade\View;

class Content extends Base
{
    //内容首页
    public function index(){

        //获取栏目列表
        $cate = M::getList([],$this->pageSize);
        $cate = tree_cate($cate);
        //halt($cate);
        $view = [
            'list' => $cate,
            'empty'=> empty_list(8),
        ];
        View::assign($view);
        return View::fetch();
    }

